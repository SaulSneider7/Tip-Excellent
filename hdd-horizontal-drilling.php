            <p>
                La PHD es un método dirigible, sin zanjas, para instalar tuberías, ductos y cables siguiendo una
                trayectoria prescrita, utilizando plataformas de perforación desde la superficie con mínimo impacto
                sobre el entorno.
            </p>

            <p>
                Se aplica cuando no es práctico establecer zanjas o excavaciones. Esta técnica minimiza las
